func (s *BackupService) CreateBackup(companyID uuid.UUID, companyCode, userID string, isAuto bool) (*models.BackupLog, error) {
	startTime := time.Now()

	if strings.TrimSpace(companyCode) == "" {
		companyCode = s.getCompanyCode(companyID)
	}
	if strings.TrimSpace(companyCode) == "" {
		companyCode = companyID.String()[:8]
	}
	companyCode = strings.ToLower(strings.TrimSpace(companyCode))

	companyDir := filepath.Join(s.backupDir, strings.ToLower(companyCode))
	os.MkdirAll(companyDir, 0755)

	timestamp := time.Now().Format("20060102_150405")
	filename := fmt.Sprintf("%s_%s.sql", strings.ToLower(companyCode), timestamp)
	filePath := filepath.Join(companyDir, filename)

	var buf bytes.Buffer

	buf.WriteString(fmt.Sprintf("-- PostgreSQL Backup\n"))
	buf.WriteString(fmt.Sprintf("-- Company: %s (ID: %s)\n", companyCode, companyID.String()))
	buf.WriteString(fmt.Sprintf("-- Backup Date: %s\n", time.Now().Format("2006-01-02 15:04:05")))
	buf.WriteString(fmt.Sprintf("-- Generated by POS Retail System\n\n"))

	buf.WriteString("SET statement_timeout = 0;\n")
	buf.WriteString("SET lock_timeout = 0;\n")
	buf.WriteString("SET client_min_messages = warning;\n")
	buf.WriteString("SET default_transaction_read_only = off;\n")
	buf.WriteString("SET client_encoding = 'UTF8';\n\n")

	tableCount := 0
	totalRows := int64(0)

	for _, tableName := range companyTables {
		rows, err := s.dumpTable(&buf, tableName, companyID, true, false)
		if err != nil {
			return nil, fmt.Errorf("error dumping table %s: %w", tableName, err)
		}
		if rows > 0 {
			tableCount++
			totalRows += rows
		}
	}

	for _, tableName := range childTables {
		rows, err := s.dumpTable(&buf, tableName, companyID, false, true)
		if err != nil {
			return nil, fmt.Errorf("error dumping table %s: %w", tableName, err)
		}
		if rows > 0 {
			tableCount++
			totalRows += rows
		}
	}

	for _, tableName := range sharedTables {
		rows, err := s.dumpTable(&buf, tableName, companyID, false, false)
		if err != nil {
			return nil, fmt.Errorf("error dumping table %s: %w", tableName, err)
		}
		if rows > 0 {
			tableCount++
			totalRows += rows
		}
	}

	err := os.WriteFile(filePath, buf.Bytes(), 0644)
	if err != nil {
		return nil, fmt.Errorf("error writing backup file: %w", err)
	}

	fileInfo, err := os.Stat(filePath)
	if err != nil {
		return nil, fmt.Errorf("error getting file info: %w", err)
	}

	backupLog := &models.BackupLog{
		CompanyID:  companyID,
		Filename:   filename,
		FilePath:   filePath,
		FileSize:   fileInfo.Size(),
		Status:     "completed",
		CreatedBy:  userID,
		IsAuto:     isAuto,
		TableCount: tableCount,
		RowCount:   totalRows,
	}

	err = s.repo.CreateBackupLog(backupLog)
	if err != nil {
		os.Remove(filePath)
		return nil, fmt.Errorf("error saving backup log: %w", err)
	}

	if isAuto {
		go s.cleanupOldBackups(companyID)
	}

	duration := time.Since(startTime)
	fmt.Printf("Backup completed: %s, %d tables, %d rows, %s\n", filename, tableCount, totalRows, duration)

	return backupLog, nil
}

func (s *BackupService) dumpTable(buf *bytes.Buffer, tableName string, companyID uuid.UUID, isCompanyTable, isChildTable bool) (int64, error) {
	var rows []map[string]interface{}
	var rowCount int64

	if isCompanyTable {
		err := s.db.Table(tableName).
			Where("company_id = ?", companyID).
			Find(&rows).Error
		if err != nil {
			return 0, err
		}
	} else if isChildTable {
		err := s.db.Table(tableName).Find(&rows).Error
		if err != nil {
			return 0, err
		}
	} else {
		err := s.db.Table(tableName).
			Where("company_id = ? OR company_id IS NULL", companyID).
			Find(&rows).Error
		if err != nil {
			return 0, err
		}
	}

	rowCount = int64(len(rows))
	if rowCount == 0 {
		return 0, nil
	}

	var columns []string
	for key := range rows[0] {
		if key != "deleted_at" {
			columns = append(columns, key)
		}
	}

	buf.WriteString(fmt.Sprintf("-- Table: %s (%d rows)\n", tableName, rowCount))
	buf.WriteString(fmt.Sprintf("COPY %s (%s) FROM stdin;\n", tableName, strings.Join(columns, ", ")))

	for _, row := range rows {
		var values []string
		for _, col := range columns {
			val := row[col]
			values = append(values, formatValue(val))
		}
		buf.WriteString(strings.Join(values, "\t") + "\n")
	}
	buf.WriteString("\\.\n\n")

	return rowCount, nil
}

func formatValue(val interface{}) string {
	if val == nil {
		return "\\N"
	}

	switch v := val.(type) {
	case time.Time:
		return v.Format("2006-01-02 15:04:05")
	case []byte:
		return strings.ReplaceAll(string(v), "\t", "\\t")
	case string:
		str := strings.ReplaceAll(v, "\\", "\\\\")
		str = strings.ReplaceAll(str, "\t", "\\t")
		str = strings.ReplaceAll(str, "\n", "\\n")
		str = strings.ReplaceAll(str, "\r", "\\r")
		if strings.ContainsAny(str, "\n\r\t\\") || str == "" {
			return str
		}
		return str
	default:
		return fmt.Sprintf("%v", v)
	}
}

