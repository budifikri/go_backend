func NewBackupService(db *gorm.DB, repo *repository.BackupRepository, cfg *config.Config) *BackupService {
	backupDir := cfg.Backup.Path
	if backupDir == "" {
		backupDir = "./backups"
	}

	pgDumpPath := cfg.Backup.PgDumpPath
	if pgDumpPath == "" {
		pgDumpPath = "pg_dump"
	}

	svc := &BackupService{
		db:           db,
		repo:         repo,
		backupDir:    backupDir,
		pgDumpPath:   pgDumpPath,
		dbConfig:     cfg.Database,
		cron:         cron.New(),
		companyCrons: make(map[uuid.UUID]cron.EntryID),
	}

	svc.ensureBackupDir()
	return svc
}

func (s *BackupService) ensureBackupDir() {
	os.MkdirAll(s.backupDir, 0755)
}

